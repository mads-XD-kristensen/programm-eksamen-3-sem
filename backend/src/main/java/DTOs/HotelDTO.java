package DTOs;

/**
 *
 * @author Mads
 */
public class HotelDTO {
    String id;
    String name;
    String price;
    String phone;
    String url;
    String email;
    String content;
    String address;

    public HotelDTO(String id, String name, String price, String phone, String url, String email, String content, String address) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.phone = phone;
        this.url = url;
        this.email = email;
        this.content = content;
        this.address = address;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    
    
}
